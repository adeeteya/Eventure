    String? koText = '',
    String? frText = '',
    String? taText = '',
    String? teText = '',
    String? mrText = '',
    String? knText = '',
    String? roText = '',
  }) =>
      [
        enText,
        esText,
        zh_HansText,
        hiText,
        bnText,
        ptText,
        ruText,
        jaText,
        deText,
        koText,
        frText,
        taText,
        teText,
        mrText,
        knText,
        roText
      ][languageIndex] ??
      '';

